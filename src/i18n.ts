function replaceTemplate(template: string, values?: Record<string, string | number>) {
  if (!values) {
    return template;
  }

  return Object.entries(values).reduce(
    (current, [key, value]) => current.replace(new RegExp(`{{${key}}}`, "g"), String(value)),
    template,
  );
}

export function normalizeLanguage(value?: string | null): Language {
  if (!value) {
    return "uz";
  }

  const normalized = value.toLowerCase();
  if (normalized.startsWith("ru")) {
    return "ru";
  }
  if (normalized.startsWith("en")) {
    return "en";
  }
  return "uz";
}

export function detectInitialLanguage(): Language {
  if (typeof window === "undefined") {
    return "uz";
  }

  const stored = window.localStorage.getItem(STORAGE_KEY);
  if (stored) {
    return normalizeLanguage(stored);
  }

  return normalizeLanguage(window.navigator.language);
}

export function persistLanguage(language: Language) {
  if (typeof window !== "undefined") {
    window.localStorage.setItem(STORAGE_KEY, language);
  }
}

export function translate(
  language: Language,
  key: string,
  values?: Record<string, string | number>,
) {
  const template = translations[language][key] ?? translations.en[key] ?? key;
  return replaceTemplate(template, values);
}

export function getLanguageName(language: Language) {
  return languageNames[language];
}

export function getOptionLabel(
  language: Language,
  group: keyof (typeof enumLabels)["uz"],
  value: string,
) {
  return enumLabels[language][group][value] ?? value.replace(/_/g, " ");
}

export function getFeatureName(language: Language, feature: string) {
  return featureLabels[language][feature] ?? feature.replace(/_/g, " ");
}

export function getFeatureValue(language: Language, value: string) {
  return featureValueLabels[language][value] ?? value.replace(/_/g, " ");
}
